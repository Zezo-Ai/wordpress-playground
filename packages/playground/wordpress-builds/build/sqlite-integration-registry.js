import fs from 'fs';
import path from 'path';

const LATEST_SQLITE_DRIVER_VERSION = 'trunk';

export function registerSqliteIntegrationVersion(
	outputDir,
	version,
	{ removeVersions = [] } = {}
) {
	const versions = loadSqliteIntegrationVersions(outputDir);

	for (const removeVersion of removeVersions) {
		delete versions[removeVersion];
	}
	versions[version] = version;

	writeSqliteIntegrationVersions(outputDir, versions);
	writeSqliteDriverModuleDetails(outputDir, versions);
}

export function getSqliteIntegrationZipPath(outputDir, version) {
	return path.resolve(
		outputDir,
		`sqlite-database-integration-${version}.zip`
	);
}

function loadSqliteIntegrationVersions(outputDir) {
	try {
		const data = fs.readFileSync(getVersionsPath(outputDir), 'utf8');
		return JSON.parse(data);
	} catch (e) {
		return {};
	}
}

function writeSqliteIntegrationVersions(outputDir, versions) {
	fs.writeFileSync(
		getVersionsPath(outputDir),
		JSON.stringify(versions, null, '\t') + '\n'
	);
}

function writeSqliteDriverModuleDetails(outputDir, versions) {
	const versionKeys = Object.keys(versions);
	const imports = versionKeys
		.map(
			(version) =>
				`// @ts-ignore
import url_${slugify(version)} from './sqlite-database-integration-${version}.zip?url';`
		)
		.join('\n');
	const cases = versionKeys
		.map(
			(version) => `
		case '${version}':
			/** @ts-ignore */
			return {
				size: ${JSON.stringify(getSqliteIntegrationZipSize(outputDir, version))},
				url: url_${slugify(version)},
			};`
		)
		.join('');
	const content = `${imports}

/**
 * This file was auto generated by:
 *   packages/playground/wordpress-builds/build/refresh-sqlite-integration-plugin.js
 *   scripts/patch-sqlite-for-php52.mjs
 * DO NOT CHANGE MANUALLY!
 * This file must statically exists in the project because of the way
 * vite resolves imports.
 */
export const LatestSqliteDriverVersion = '${LATEST_SQLITE_DRIVER_VERSION}';

export function getSqliteDriverModuleDetails(
	version = LatestSqliteDriverVersion
): {
	size: number;
	url: string;
} {
	switch (version) {${cases}
	}
	throw new Error(
		'Unsupported SQLite integration plugin version: ' + version
	);
}
`;

	fs.writeFileSync(
		path.join(outputDir, 'get-sqlite-driver-module-details.ts'),
		content
	);
}

function getSqliteIntegrationZipSize(outputDir, version) {
	try {
		return fs.statSync(getSqliteIntegrationZipPath(outputDir, version))
			.size;
	} catch (e) {
		return 0;
	}
}

function getVersionsPath(outputDir) {
	return path.join(outputDir, 'sqlite-database-integration-versions.json');
}

function slugify(v) {
	return v.replace(/[^a-zA-Z0-9_]/g, '_');
}
